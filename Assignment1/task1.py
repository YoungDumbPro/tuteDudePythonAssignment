# Perform Basic Mathematical Operations


# Taking float input from user as they can enter decimal values also
print("Enter first number: ")

num1 = float(input())

print("Enter second number: ")

num2 = float(input())

# Addition
sum = num1 + num2

print("Addition: ", sum)

# Subtraction
difference = num1 - num2       
print("Subtraction: ", difference)  

# Multiplication
product = num1 * num2   
print("Multiplication: ", product)

# Division
quotient = num1 / num2          
print("Division: ", quotient)


